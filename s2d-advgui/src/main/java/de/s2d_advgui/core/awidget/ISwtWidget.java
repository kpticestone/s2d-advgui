package de.s2d_advgui.core.awidget;

import com.badlogic.gdx.scenes.scene2d.Actor;
import com.badlogic.gdx.scenes.scene2d.Group;

import de.s2d_advgui.core.layoutmanager.ASwtLayoutManager;
import de.s2d_advgui.core.rendering.ISwtDrawerManager;
import de.s2d_advgui.core.resourcemanager.AResourceManager;
import de.s2d_advgui.core.stage.ISwtStage;
import de.s2d_advgui.core.utils.IRectangleSupport;

import javax.annotation.Nonnull;
import javax.annotation.Nullable;
import java.util.Collection;
import java.util.List;

public interface ISwtWidget<PT extends Actor> extends IRectangleSupport {
    // -------------------------------------------------------------------------------------------------------------------------
    PT getActor();

    // -------------------------------------------------------------------------------------------------------------------------
    Collection<ISwtWidget<?>> getChildren();

    // -------------------------------------------------------------------------------------------------------------------------
    boolean calcPositions();

    // -------------------------------------------------------------------------------------------------------------------------
    boolean isEnabled();

    // -------------------------------------------------------------------------------------------------------------------------
    boolean isVisible();

    // -------------------------------------------------------------------------------------------------------------------------
    void setVisible(boolean pVisible);

    // -------------------------------------------------------------------------------------------------------------------------
    default boolean setEnabled() {
        return this.setEnabled(true);
    }

    // -------------------------------------------------------------------------------------------------------------------------
    default boolean setDisabled() {
        return this.setEnabled(false);
    }

    // -------------------------------------------------------------------------------------------------------------------------
    boolean setEnabled(boolean pEnabled);

    // -------------------------------------------------------------------------------------------------------------------------
    void calculateTabOrderList(List<ISwtWidget<?>> pOrderedList);

    // -------------------------------------------------------------------------------------------------------------------------
    void act(float pDelta);

    // -------------------------------------------------------------------------------------------------------------------------
    boolean focus();

    // -------------------------------------------------------------------------------------------------------------------------
    @Nonnull
    ISwtStage<?, ?> getContext();

    // -------------------------------------------------------------------------------------------------------------------------
    boolean getIgnoreOnCalcPos();

    // -------------------------------------------------------------------------------------------------------------------------
    void dispose();

    // -------------------------------------------------------------------------------------------------------------------------
    void setBounds(float pX, float pY, float pWidth, float pHeight);

    // -------------------------------------------------------------------------------------------------------------------------
    void setPosition(float pX, float pY);

    // -------------------------------------------------------------------------------------------------------------------------
    void setSize(float pX, float pY);

    // -------------------------------------------------------------------------------------------------------------------------
    @Nullable
    ISwtWidget<? extends Group> getParent();

    // -------------------------------------------------------------------------------------------------------------------------
    ISwtWidget<? extends Group> getDelegatedParent();

    // -------------------------------------------------------------------------------------------------------------------------
    float getWidth();

    // -------------------------------------------------------------------------------------------------------------------------
    float getHeight();

    // -------------------------------------------------------------------------------------------------------------------------
    float getX();

    // -------------------------------------------------------------------------------------------------------------------------
    float getY();

    // -------------------------------------------------------------------------------------------------------------------------
    void setLayoutManager(ASwtLayoutManager pManager);

    // -------------------------------------------------------------------------------------------------------------------------
    boolean isHovered();

    // -------------------------------------------------------------------------------------------------------------------------
    default void onChildAdded(ISwtWidget<?> a) {
        ISwtWidget<? extends Group> parent = getParent();
        if (parent != null) {
            parent.onChildAdded(a);
        }
    }

    // -------------------------------------------------------------------------------------------------------------------------
    @Nullable
    ASwtLayoutData getSwtLayoutData();

    // -------------------------------------------------------------------------------------------------------------------------
    void bringToFront(ISwtWidget<?> someWidget);

    // -------------------------------------------------------------------------------------------------------------------------
}
