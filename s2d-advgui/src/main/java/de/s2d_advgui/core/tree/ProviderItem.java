package de.s2d_advgui.core.tree;

public class ProviderItem {
    // -------------------------------------------------------------------------------------------------------------------------
    private final String uid;

    // -------------------------------------------------------------------------------------------------------------------------
    public String label;

    // -------------------------------------------------------------------------------------------------------------------------
    public String icon;

    // -------------------------------------------------------------------------------------------------------------------------
    public ProviderItem(String pUid, String pLabel) {
        this.uid = pUid;
        this.label = pLabel;
    }

    // -------------------------------------------------------------------------------------------------------------------------
    public ProviderItem(String pUid, String pLabel, String pIcon) {
        this.uid = pUid;
        this.label = pLabel;
        this.icon = pIcon;
    }

    // -------------------------------------------------------------------------------------------------------------------------
    public void setIcon(String icon) {
        this.icon = icon;
    }

    // -------------------------------------------------------------------------------------------------------------------------
    @Override
    public boolean equals(Object obj) {
        if (obj instanceof ProviderItem) return ((ProviderItem) obj).uid.contentEquals(this.uid);
        return false;
    }

    // -------------------------------------------------------------------------------------------------------------------------
    @Override
    public int hashCode() {
        return this.uid.hashCode();
    }

    // -------------------------------------------------------------------------------------------------------------------------
}
